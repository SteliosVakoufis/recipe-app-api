"""
Calculator functions
"""


def add(x, y):
    """Adds two numbers together and returns the result

    Args:
        x (number): first number
        y (number): second number
    """
    return x + y


def sub(x, y):
    """Subtracts two numbers from each other and returns the result

    Args:
        x (number): first number
        y (number): second number
    """
    return x - y
